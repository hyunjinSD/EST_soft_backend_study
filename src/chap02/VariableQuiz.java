package chap02;

public class VariableQuiz {

    public static void main(String[] args) {
        int x = 1;
        int y = 20;
        int z = 300;

        // 코드를 작성해주세요
        x = y;
        y = z;
        z = 1;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
    }
}
